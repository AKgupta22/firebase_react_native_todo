import React from "react";
import { Modal, StyleSheet, View } from "react-native";
import Form from "./Form";
import TodoEditForm from "./TodoEditForm";

const ModalForm = ({ modalVisible, setModalVisible, fetchData, index }) => {
  return (
    <View style={styles.centeredView}>
      <Modal
        animationType="slide"
        transparent={true}
        visible={modalVisible}
        onRequestClose={() => {
          setModalVisible(!modalVisible);
        }}
      >
        <View style={styles.centeredView}>
          <View style={styles.modalView}>
            {index ? (
              <TodoEditForm setModalVisible={setModalVisible} index={index} fetchData={fetchData} />
            ) : (
              <Form setModalVisible={setModalVisible} fetchData={fetchData} />
            )}
          </View>
        </View>
      </Modal>
    </View>
  );
};

const styles = StyleSheet.create({
  centeredView: {
    flex: 1,
    justifyContent: "center",
    alignItems: "center",
    marginTop: 12,
  },
  modalView: {
    margin: 20,
    backgroundColor: "white",
    borderRadius: 20,
    padding: 35,
    alignItems: "center",
    shadowColor: "#000",
    shadowOffset: {
      width: 0,
      height: 2,
    },
    shadowOpacity: 0.25,
    shadowRadius: 4,
    elevation: 5,
  },
});

export default ModalForm;
